    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Contato contato = new Contato();
        ContatoDao contatoDao = null;
        try {
            contatoDao = new ContatoDao();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ContatoNovoUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        contato.setNome(jTextField1.getText());
        contato.setEmail(jTextField2.getText());
        contato.setEndereco(jTextField3.getText());
        contato.setTelefone(jTextField4.getText());
        contato.setId(this.id);
        
        contatoDao.altera(contato);
        this.setVisible(false); 
    }//GEN-LAST:event_jButton1ActionPerformed

